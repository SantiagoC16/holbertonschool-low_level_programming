#include "3-calc.h"

/**
 * op_add - suma
 *
 * @a: is an int
 * @b: is an int
 *
 * Return: 0
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - resta
 *
 * @a: is an int
 * @b: is an int
 *
 * Return: 0
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplicar
 *
 * @a: is an int
 * @b: is an int
 *
 * Return: 0
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - dividir
 *
 * @a: is an int
 * @b: is an int
 *
 * Return: 0
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulo
 *
 * @a: is an int
 * @b: is an int
 *
 * Return: 0
 */

int op_mod(int a, int b)
{
return (a % b);
}

